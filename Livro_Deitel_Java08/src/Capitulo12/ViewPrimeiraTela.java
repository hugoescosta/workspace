	/**
	 * Create the application.
	 */
	public ViewPrimeiraTela() {
		initialize();
	}

	/**
	 * Initialize the contents of the frame.
	 */
	private void initialize() {
		frame = new JFrame();
		frame.setBounds(100, 100, 1027, 592);
		frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		frame.getContentPane().setLayout(null);
		
		JLabel lblNome = new JLabel("Nome");
		lblNome.setFont(new Font("Tahoma", Font.PLAIN, 16));
		lblNome.setBounds(81, 87, 80, 27);
		frame.getContentPane().add(lblNome);
		
		tfNome = new JTextField();
		tfNome.setBounds(81, 123, 505, 22);
		frame.getContentPane().add(tfNome);
		tfNome.setColumns(10);
		
		JButton btnMostrar = new JButton("Mostrar");
		btnMostrar.addActionListener(new ActionListener() {
			public void actionPerformed(ActionEvent e) {
				JOptionPane.showMessageDialog(null,"Nome: "+ tfNome.getText());
			}
		});
		btnMostrar.setBounds(81, 158, 97, 25);
		frame.getContentPane().add(btnMostrar);
	}
