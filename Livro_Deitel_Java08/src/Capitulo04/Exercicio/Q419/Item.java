package Capitulo04.Exercicio.Q419;

public class Item {
	private String item;
	private double valor;
	
	//construtor
	public Item(String item, double valor) {
		this.item = item;
		this.valor = valor;
	}
	
	
	//getters e setters
	
	
	public String getItem() {
		return item;
	}
	public double getValor() {
		return valor;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	
	
}
